#include "libft.h"

void	*ft_memccpy(void *restrict dst, const void *restrict src,
		int c, size_t n)
{
	unsigned char	*dst_ptr;
	size_t			i;

	i = -1;
	dst_ptr = dst;
	while (++i < n)
	{
		dst_ptr[i] = ((unsigned char *)src)[i];
		if (((unsigned char *)src)[i] == (unsigned char)c)
			return (dst + i + 1);
	}
	return (NULL);
}
