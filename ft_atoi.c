#include "libft.h"

int		ft_atoi(const char *str)
{
	int		sign;
	int		i;
	int		result;

	result = 0;
	i = 0;
	sign = 1;
	while (str[i] == ' ' || ('\a' <= str[i] && str[i] <= '\r'))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while ('0' <= str[i] && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	return (result * sign);
}
